export default function weekday(nr){
  switch (nr) {
    case 1:
    return 'Måndag'
    break;
    case 2:
    return 'Tisdag'
    break;
    case 3:
    return 'Onsdag'
    break;
    case 4:
    return 'Torsdag'
    break;
    case 5:
    return 'Fredag'
    break;
    case 6:
    return 'Lördag'
    break;
    case 7:
    return 'Söndag'
    break;
    case 8:
    return ''
    break;
    default:
    break;

  }
}
